class Bottle:
    color = ''
    volume = 0.0


bottle_1 = Bottle()
bottle_1.color = 'Красная'
bottle_1.volume = 0.7
bottle_2 = Bottle()
bottle_2.color = 'Белая'
bottle_2.volume = 0.3
bottle_3 = Bottle()
bottle_3.color = 'Черная'
bottle_3.volume = 0.1
print(bottle_1.color, bottle_1.volume)
print(bottle_2.color, bottle_2.volume)
print(bottle_3.color, bottle_3.volume)
